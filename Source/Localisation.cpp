void install(Language lang)
{
    const char* table = (lang == Language::English) ? englishTranslations
                                                     : japaneseTranslations;
    juce::LocalisedStrings::setCurrentMappings(
        new juce::LocalisedStrings(juce::String(juce::CharPointer_UTF8(table)),
                                   /*ignoreCaseOfKeys*/ false));
}

void installJapanese() { install(Language::Japanese); }

static juce::File languageStoreFile()
{
    auto root = juce::File::getSpecialLocation(juce::File::userApplicationDataDirectory)
                    .getChildFile("Trakova");
    root.createDirectory();
    return root.getChildFile("language.txt");
}

Language getSavedLanguage()
{
    auto f = languageStoreFile();
    if (f.existsAsFile())
    {
        // ユーザが明示的に選んだ言語を優先
        const auto txt = f.loadFileAsString().trim().toLowerCase();
        if (txt == "english" || txt == "en")    return Language::English;
        if (txt == "japanese" || txt == "ja")   return Language::Japanese;
    }
    // 初回起動 (未保存): システム言語に合わせる。日本語のみ日本語、それ以外は英語。
    const auto sys = juce::SystemStats::getUserLanguage().toLowerCase();
    return sys.startsWith("ja") ? Language::Japanese : Language::English;
}

void saveLanguage(Language lang)
{
    languageStoreFile().replaceWithText(lang == Language::English ? "english" : "japanese");
}

juce::String displayName(Language lang)
{
    return lang == Language::English ? "English" : juce::String::fromUTF8(u8"日本語");
}

